def get_polish_prompt_with_examples(polish_type: str, text: str) -> str:
    """
    获取带有示例的润色提示词

    Args:
        polish_type: 润色类型
        text: 待润色文本

    Returns:
        完整提示词
    """
    template = ADVANCED_POLISH_PROMPTS.get(polish_type, ADVANCED_POLISH_PROMPTS["general"])

    # 如果有示例，可以选择性地添加到提示词中
    examples = FEW_SHOT_EXAMPLES.get(polish_type, [])
    if examples:
        # 对于已经有示例的模板，不需要额外添加
        pass

    return template.format(text=text)


def get_example_before_after(example_type: str, index: int = 0) -> tuple:
    """
    获取指定类型的示例

    Args:
        example_type: 示例类型
        index: 示例索引

    Returns:
        (before, after) 元组
    """
    examples = FEW_SHOT_EXAMPLES.get(example_type, [])
    if 0 <= index < len(examples):
        example = examples[index]
        return example["before"], example["after"]
    return "", ""
