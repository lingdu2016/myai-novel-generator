def get_advanced_template(category: str, name: str) -> str:
    """
    获取高级预设模板

    Args:
        category: 类别（rewrite/generation/outline等）
        name: 模板名称

    Returns:
        模板内容
    """
    if category == "rewrite":
        key = f"重写风格 - {name}"
        return ADVANCED_PRESET_TEMPLATES.get(key, ADVANCED_PRESET_TEMPLATES["重写风格 - 默认"])

    # 其他类别可以继续扩展
    return ""


def list_advanced_templates(category: Optional[str] = None) -> Dict[str, List[str]]:
    """
    列出高级模板

    Args:
        category: 类别，None表示所有

    Returns:
        {category: [template_names]}
    """
    result = {}

    if category == "rewrite" or category is None:
        rewrite_names = [k.replace("重写风格 - ", "") for k in ADVANCED_PRESET_TEMPLATES.keys() if k.startswith("重写风格")]
        result["rewrite"] = rewrite_names

    return result


def get_technique_example(technique: str) -> str:
    """获取技法示例"""
    return TECHNIQUE_EXAMPLES.get(technique, "")
