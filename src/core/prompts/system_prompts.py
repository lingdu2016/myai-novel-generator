def get_system_prompt(prompt_type: str, genre: str = None) -> str:
    """
    获取完整的 System Prompt

    Args:
        prompt_type: 提示词类型 (novel_writer, outline_planner等)
        genre: 小说类型（可选）

    Returns:
        完整的 System Prompt
    """
    base_prompt = SYSTEM_PROMPTS.get(prompt_type, SYSTEM_PROMPTS["novel_writer"])

    if genre and genre in GENRE_SYSTEM_PROMPTS:
        genre_prompt = GENRE_SYSTEM_PROMPTS[genre]
        return f"{base_prompt}\n\n【题材专属】\n{genre_prompt}"

    return base_prompt


